p1 = (-2, 4)
p3 = (-1, -2)
p4 = (3, 5)
p5 = (4.3, 11)
x = 7.7

def fn(x, p1, p3, p4, p5):
    r1=(p3[1]-p1[1])/(p3[0]-p1[0])
    r2=((p4[1]-p3[1])/(p4[0]-p3[0])-r1)/(p4[0]-p1[0])
    r3=((((p5[1]-p4[1])/(p5[0]-p4[0]))-((p4[1]-p3[1]) / (p4[0]-p3[0])))/(p5[0]-p3[0])-r2)/(p5[0]-p1[0])
    r4=(p1[1]+r1*(x-p1[0])+r2*((x-p1[0])*(x-p3[0])) + r3*(x-p1[0])*(x-p3[0])*(x-p4[0]))
    return r4

print("El valor de X es = ", x, "y el valor de y es: ", fn(x, p1, p3, p4, p5))